PRONOUNS = ["yo", "tú", "él / ella / usted", "nosotros",
            "vosotros", "ellos / ellas / ustedes"]

# short pronoun labels for compact reference tables
PRON_SHORT = ["yo", "tú", "él/ella/ud.", "nosotros", "vosotros", "ellos/uds."]


def _zip(forms):
    return list(zip(PRONOUNS, forms))


# ══════════════════════════════════════════════════════════════════════════
# REGULAR CONJUGATORS  — the reliable machinery
# ══════════════════════════════════════════════════════════════════════════
_PRESENT = {
    "ar": ["o", "as", "a", "amos", "áis", "an"],
    "er": ["o", "es", "e", "emos", "éis", "en"],
    "ir": ["o", "es", "e", "imos", "ís", "en"],
}
_PRETERITE = {
    "ar": ["é", "aste", "ó", "amos", "asteis", "aron"],
    "er": ["í", "iste", "ió", "imos", "isteis", "ieron"],
    "ir": ["í", "iste", "ió", "imos", "isteis", "ieron"],
}
_IMPERFECT = {
    "ar": ["aba", "abas", "aba", "ábamos", "abais", "aban"],
    "er": ["ía", "ías", "ía", "íamos", "íais", "ían"],
    "ir": ["ía", "ías", "ía", "íamos", "íais", "ían"],
}
_FUTURE = ["é", "ás", "á", "emos", "éis", "án"]  # added to the whole infinitive


def stem(inf):
    return inf[:-2]


def ending(inf):
    return inf[-2:]


def present(inf):
    return [stem(inf) + e for e in _PRESENT[ending(inf)]]


def preterite(inf):
    return [stem(inf) + e for e in _PRETERITE[ending(inf)]]


def imperfect(inf):
    return [stem(inf) + e for e in _IMPERFECT[ending(inf)]]


def future(inf):
    return [inf + e for e in _FUTURE]


def conj(inf, tense):
    return _zip({"present": present, "preterite": preterite,
                 "imperfect": imperfect, "future": future}[tense](inf))


