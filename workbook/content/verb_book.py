# Preterite vs imperfect — the decision guide
PRET_VS_IMPERF = [
    ("A single, completed action", "PRETERITE", "Ayer fui a la playa.", "Yesterday I went to the beach."),
    ("What happened next (a sequence)", "PRETERITE", "Llegué, nadé y comí.", "I arrived, swam, and ate."),
    ("A sudden interruption", "PRETERITE", "Sonó el teléfono.", "The phone rang."),
    ("Used-to / habitual in the past", "IMPERFECT", "De niña nadaba cada día.", "As a girl I used to swim every day."),
    ("Background, weather, time, age", "IMPERFECT", "Eran las ocho y hacía calor.", "It was eight o'clock and it was hot."),
    ("An ongoing action (was …-ing)", "IMPERFECT", "Yo cocinaba cuando llamó.", "I was cooking when he called."),
]


